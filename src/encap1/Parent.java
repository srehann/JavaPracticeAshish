package encap1;

public class Parent {
	
	private int salary;
	private int age;
	private String Name;
	public int getSalary() {
		return salary;
	}
	public void setSalary(int salary) {
		this.salary = salary;
	}
	
	public void setAge(int age) {
		this.age = age;

	}
	public int getAge() {
		return age;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}

}
